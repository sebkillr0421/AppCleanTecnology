package h2h.iot.panic;

public class MqttCloud {


    public String flow;
    public String Tamb;
    public String Tfilter;
    public String Pressure;
    public String dirWin;
    public String speedWin;
    public String rain;


    public String getFlow() {
        return flow;
    }

    public void setFlow(String flow) {
        this.flow = flow;
    }

    public String getTamb() {
        return Tamb;
    }

    public void setTamb(String tamb) {
        Tamb = tamb;
    }

    public String getTfilter() {
        return Tfilter;
    }

    public void setTfilter(String tfilter) {
        Tfilter = tfilter;
    }

    public String getPressure() {
        return Pressure;
    }

    public void setPressure(String pressure) {
        Pressure = pressure;
    }

    public String getDirWin() {
        return dirWin;
    }

    public void setDirWin(String dirWin) {
        this.dirWin = dirWin;
    }

    public String getSpeedWin() {
        return speedWin;
    }

    public void setSpeedWin(String speedWin) {
        this.speedWin = speedWin;
    }

    public String getRain() {
        return rain;
    }

    public void setRain(String rain) {
        this.rain = rain;
    }







}
